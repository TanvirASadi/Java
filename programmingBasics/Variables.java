package programmingBasics;

public class Variables {
	
	//Variables are container or place holders to hold a value for a specific entitiy. 
	//The example below demonstrates how a specific value is stored to use is later where needed. 
	public static void main(String[] args) {
		
		String nameString = "Tanvir";
		int age = 30;
		double salary = 15.25;
		
		System.out.println("The name of this employee is " + nameString + ", he is " + age + "\n and his salary is + " + salary);
	}

}
